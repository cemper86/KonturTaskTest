package ru.stairenx.konturtasktest.item;

public class EducationPeriod {

    String start;
    String finish;

    public EducationPeriod(String start, String finish) {
        this.start = start;
        this.finish = finish;
    }

    public String getStart() {
        return start;
    }

    public String getFinish() {
        return finish;
    }
}
